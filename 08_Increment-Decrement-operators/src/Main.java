public class Main{
    public static void main(String[] args) {

        // Increment and decrement operators

        int x=1;
        System.out.println("Increment...");
        System.out.print("\n");
        System.out.println(x);
        x++;
        System.out.println(x);
        x++;
        System.out.println(x);
        x++;
        System.out.println(x);
        System.out.println("Decrement...");
        System.out.print("\n");
        System.out.println(x);
        x--;
        System.out.println(x);
        x--;
        System.out.println(x);
        x--;
        System.out.println(x);
    }
}
