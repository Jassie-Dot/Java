public class Main {
    public static void main(String[] args) {
        // MATH CLASS

        double result;
        result=Math.pow(2,3); // 2 power 3
        System.out.println(result);
        result=Math.abs(-5); // Finding the absolute value of -5.
        System.out.println(result);
        result=Math.sqrt(9); // Finding the square root.
        System.out.println(result);
        result=Math.round(3.14); // Finding the round.
        System.out.println(result);
        result=Math.ceil(3.14);
        System.out.println(result);
        result=Math.floor(3.99);
        System.out.println(result);
        result=Math.max(2,3);
        System.out.println(result);
        result=Math.min(2,3);
        System.out.println(result);
    }
}